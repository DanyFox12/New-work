package com.upx.builder.i18n

/**
 * Supported user-interface languages for upxBuilder.
 *
 * Arabic is right-to-left; the UI mirrors its layout direction automatically.
 */
enum class AppLanguage(val code: String, val displayName: String, val rtl: Boolean) {
    ENGLISH("en", "English", false),
    ARABIC("ar", "العربية", true),
    CHINESE("zh", "中文", false),
    ITALIAN("it", "Italiano", false);

    companion object {
        fun fromCode(code: String): AppLanguage = entries.firstOrNull { it.code == code } ?: ENGLISH
    }
}

